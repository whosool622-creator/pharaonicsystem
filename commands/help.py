from vkbottle.bot import Bot, Message
from models.role import RoleManager, RoleLevel, ROLE_NAMES


def register(bot: Bot):
    
    @bot.on.chat_message(text=["/help", "/помощь", "/хелп", "help", "помощь", "хелп"])
    async def help_cmd(message: Message):
        """Показывает справку по всем командам или по конкретной команде"""
        
        args = message.text.split()
        user_role = await RoleManager.get_role(message.from_id, message.peer_id)
        
        # Если запрошена справка по конкретной команде
        if len(args) > 1:
            command = args[1].lower().lstrip("/")
            help_text = get_command_help(command, user_role)
            
            if help_text:
                await message.answer(help_text)
            else:
                await message.answer(
                    f"❌ Команда '/{command}' не найдена.\n"
                    f"Используйте /help для просмотра всех команд."
                )
            return
        
        # Общая справка
        response = f"📚 **СПРАВКА ПО КОМАНДАМ**\n\n"
        response += f"👤 Ваша роль: {ROLE_NAMES[user_role]}\n"
        response += f"💡 Префикс команд: /\n\n"
        
        response += "━━━━━━━━━━━━━━━━━━━━━━\n"
        response += "📋 **ОСНОВНЫЕ КОМАНДЫ**\n"
        response += "━━━━━━━━━━━━━━━━━━━━━━\n\n"
        
        response += "**/menu** — показать панель модератора\n"
        response += "**/help** — эта справка\n"
        response += "**/help [команда]** — справка по конкретной команде\n\n"
        
        response += "━━━━━━━━━━━━━━━━━━━━━━\n"
        response += "🆔 **ИНФОРМАЦИЯ**\n"
        response += "━━━━━━━━━━━━━━━━━━━━━━\n\n"
        
        response += "**/info** [@user] — информация о пользователе\n"
        response += "**/get_id** [@user] — узнать ID пользователя/беседы\n"
        response += "**/chat_id** — ID и ссылка на беседу\n"
        response += "**/my_id** — ваш ID\n\n"
        
